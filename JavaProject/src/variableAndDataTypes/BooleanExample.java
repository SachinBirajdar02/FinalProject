package variableAndDataTypes;

public class BooleanExample {

	public static void main(String[] args) {
		
		//declaring the variable
		boolean var1;
		
		//initializing the variable
		var1=true;
		
		System.out.println(var1);
		
		//re-initializing
		var1=false;
		System.out.println(var1);
		
		
		
	}

}
